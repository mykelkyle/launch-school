class CircularQueue
  def initialize(buffer_length)
    @buffer_length = buffer_length
    @queue = Array.new
  end

  def enqueue(val)
    if @queue.size == @buffer_length
      dequeue
      @queue.push(val)
    else
      @queue.push(val)
    end
  end

  def dequeue
    @queue.shift
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

